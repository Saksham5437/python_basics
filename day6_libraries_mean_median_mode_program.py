#program to display mean of a given numbers using statistics module

import statistics

mean = statistics.mean([100, 80, 89, 99, 89, 95])
print(mean)

#program to display median of set of numbers using statistics module

med = statistics.median([45, 56, 72, 15, 26, 19,])
print(med)

#program to display mode of set of numbers using statistics module

mod = statistics.mode([2, 5, 6, 2, 7, 8, 1, 2, 3, 2])
print(mod)